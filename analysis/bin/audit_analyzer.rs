fn tracklist_key(feed: &stophammer_parser::types::IngestFeedData) -> String {
    normalized_track_titles(feed).join(" | ")
}

fn normalized_track_titles(feed: &stophammer_parser::types::IngestFeedData) -> Vec<String> {
    let titles: Vec<String> = feed
        .tracks
        .iter()
        .map(|track| normalized(&track.title))
        .collect();
    titles
}

fn build_report(analyzer: Analyzer) -> Report {
    let duplicate_title_author_clusters = analyzer
        .title_author_clusters
        .values()
        .filter(|feeds| feeds.len() > 1)
        .count();
    let cross_platform_title_author_clusters = analyzer
        .title_author_clusters
        .values()
        .filter(|feeds| {
            if feeds.len() < 2 {
                return false;
            }
            let platforms: HashSet<&str> =
                feeds.iter().map(|feed| feed.platform.as_str()).collect();
            platforms.len() > 1
        })
        .count();
    let exact_tracklist_clusters = analyzer
        .exact_tracklist_clusters
        .values()
        .filter(|feeds| feeds.len() > 1)
        .count();
    let release_candidates = all_release_candidates(&analyzer.release_candidate_clusters);
    let recording_candidates = all_recording_candidates(&analyzer.recording_candidate_clusters);
    let mirrored_release_candidates = release_candidates
        .iter()
        .filter(|cluster| cluster.feed_count > 1)
        .count();
    let cross_platform_release_candidates = release_candidates
        .iter()
        .filter(|cluster| cluster.distinct_platforms > 1)
        .count();
    let multi_item_recording_candidates = recording_candidates
        .iter()
        .filter(|cluster| cluster.item_count > 1)
        .count();
    let cross_platform_recording_candidates = recording_candidates
        .iter()
        .filter(|cluster| cluster.distinct_platforms > 1)
        .count();

    Report {
        corpus: CorpusSummary {
            total_rows: analyzer.total_rows,
            rows_with_raw_xml: analyzer.rows_with_raw_xml,
            http_status_counts: format_counter(&analyzer.http_status_counts),
            fetch_error_rows: analyzer.fetch_error_rows,
            ndjson_parse_error_rows: analyzer.ndjson_parse_error_rows,
            xml_reparse_successes: analyzer.xml_reparse_successes,
            xml_reparse_failures: analyzer.xml_reparse_failures,
            top_hosts: top_counts(&analyzer.host_counts, 10),
            top_platforms: top_counts(&analyzer.platform_counts, 10),
        },
        parsed_shape: ParsedShapeSummary {
            medium_counts: format_counter(&analyzer.medium_counts),
            track_count_histogram: analyzer
                .track_count_histogram
                .iter()
                .map(|(k, v)| (k.to_string(), *v))
                .collect(),
            max_track_count: analyzer.max_track_count,
            single_track_feeds: analyzer.single_track_feeds,
            multi_track_feeds: analyzer.multi_track_feeds,
            top_owner_names: top_counts(&analyzer.owner_name_counts, 10),
            top_author_names: top_counts(&analyzer.author_name_counts, 10),
        },
        remote_items: RemoteItemSummary {
            feeds_with_remote_items: analyzer.feeds_with_remote_items,
            total_remote_items: analyzer.total_remote_items,
            remote_medium_counts: format_counter(&analyzer.remote_medium_counts),
            top_remote_targets: top_remote_targets(&analyzer.remote_target_counts, 20),
        },
        live_items: LiveItemSummary {
            feeds_with_live_items: analyzer.feeds_with_live_items,
            total_live_items: analyzer.total_live_items,
            status_counts: format_counter(&analyzer.live_status_counts),
        },
        contributor_signals: ContributorSummary {
            feeds_with_feed_level_persons: analyzer.feeds_with_feed_level_persons,
            tracks_with_item_level_persons: analyzer.tracks_with_item_level_persons,
            total_feed_level_person_tags: analyzer.total_feed_level_person_tags,
            total_item_level_person_tags: analyzer.total_item_level_person_tags,
            top_roles: top_role_counts(&analyzer.person_role_counts, 20),
            top_groups: top_group_counts(&analyzer.person_group_counts, 10),
        },
        mirror_signals: MirrorSummary {
            duplicate_title_author_clusters,
            cross_platform_title_author_clusters,
            exact_tracklist_clusters,
            top_title_author_clusters: summarize_clusters(&analyzer.title_author_clusters, 20),
            top_exact_tracklist_clusters: summarize_exact_tracklist_clusters(
                &analyzer.exact_tracklist_clusters,
                20,
            ),
        },
        release_candidates: ReleaseCandidateSummary {
            total_candidates: release_candidates.len(),
            mirrored_candidates: mirrored_release_candidates,
            cross_platform_candidates: cross_platform_release_candidates,
            top_candidates: release_candidates.into_iter().take(20).collect(),
        },
        recording_candidates: RecordingCandidateSummary {
            total_candidates: recording_candidates.len(),
            multi_item_candidates: multi_item_recording_candidates,
            cross_platform_candidates: cross_platform_recording_candidates,
            top_candidates: recording_candidates.into_iter().take(20).collect(),
        },
        recommendations: vec![
            "Treat feed_audit.ndjson as a raw-XML corpus; the parsed sections are hypotheses, not canonical truth.".to_string(),
            "Model feeds and items as source-layer entities first, then resolve canonical artist/release/recording entities above them.".to_string(),
            "Use feed-level podcast:remoteItem as the strongest source-side relationship signal for linking release feeds to artist or catalog feeds.".to_string(),
            "Do not use owner_name as canonical artist identity; in this corpus it is often a platform/publisher label rather than the performer.".to_string(),
            "Use duplicate title+author and exact-tracklist cross-platform clusters as evidence for source-feed mirrors rather than treating every feed as a distinct release.".to_string(),
            "Stage podcast:person as contributor evidence with role/group provenance before attempting canonical contributor or artist-credit merges.".to_string(),
        ],
    }
}

