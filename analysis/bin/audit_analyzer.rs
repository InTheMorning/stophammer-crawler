fn main() -> Result<(), Box<dyn std::error::Error>> {
    let cli = Cli::parse();
    for path in [
        &cli.output_md,
        &cli.output_json,
        &cli.output_release_json,
        &cli.output_recording_json,
    ] {
        if let Some(parent) = std::path::Path::new(path).parent()
            && !parent.as_os_str().is_empty()
        {
            fs::create_dir_all(parent)?;
        }
    }
    let parser = profile::stophammer();
    let file = File::open(&cli.input)?;
    let reader = BufReader::new(file);
    let mut analyzer = Analyzer::default();

    for (index, line) in reader.lines().enumerate() {
        if let Some(limit) = cli.limit
            && index >= limit
        {
            break;
        }

        let line = line?;
        let row: AuditRow = serde_json::from_str(&line)?;
        analyzer.total_rows += 1;
        if row.parse_error.is_some() {
            analyzer.ndjson_parse_error_rows += 1;
        }
        if let Some(status) = row.fetch.http_status {
            *analyzer
                .http_status_counts
                .entry(status.to_string())
                .or_default() += 1;
        }
        if row.fetch.error.is_some() {
            analyzer.fetch_error_rows += 1;
        }

        let host = row.host.unwrap_or_else(|| "(unknown)".to_string());
        let platform = platform_for_host(&host);
        *analyzer.host_counts.entry(host.clone()).or_default() += 1;
        *analyzer
            .platform_counts
            .entry(platform.clone())
            .or_default() += 1;

        let Some(raw_xml) = row.raw_xml.as_deref() else {
            continue;
        };
        analyzer.rows_with_raw_xml += 1;

        let reparsed = match parser.parse(raw_xml) {
            Ok(feed) => {
                analyzer.xml_reparse_successes += 1;
                feed
            }
            Err(_) => {
                analyzer.xml_reparse_failures += 1;
                continue;
            }
        };

        let medium = reparsed
            .raw_medium
            .clone()
            .unwrap_or_else(|| "(none)".to_string());
        *analyzer.medium_counts.entry(medium).or_default() += 1;
        let track_count = reparsed.tracks.len();
        *analyzer
            .track_count_histogram
            .entry(track_count)
            .or_default() += 1;
        analyzer.max_track_count = analyzer.max_track_count.max(track_count);
        if track_count <= 1 {
            analyzer.single_track_feeds += 1;
        } else {
            analyzer.multi_track_feeds += 1;
        }

        let owner_name = reparsed
            .owner_name
            .clone()
            .unwrap_or_else(|| "(none)".to_string());
        let author_name = reparsed
            .author_name
            .clone()
            .unwrap_or_else(|| "(none)".to_string());
        *analyzer.owner_name_counts.entry(owner_name).or_default() += 1;
        *analyzer
            .author_name_counts
            .entry(author_name.clone())
            .or_default() += 1;

        if !reparsed.remote_items.is_empty() {
            analyzer.feeds_with_remote_items += 1;
            analyzer.total_remote_items += reparsed.remote_items.len();
            for remote in &reparsed.remote_items {
                let medium = remote
                    .medium
                    .clone()
                    .unwrap_or_else(|| "(none)".to_string());
                *analyzer.remote_medium_counts.entry(medium).or_default() += 1;
                *analyzer
                    .remote_target_counts
                    .entry(remote.remote_feed_guid.clone())
                    .or_default() += 1;
            }
        }

        if !reparsed.live_items.is_empty() {
            analyzer.feeds_with_live_items += 1;
            analyzer.total_live_items += reparsed.live_items.len();
            for live_item in &reparsed.live_items {
                *analyzer
                    .live_status_counts
                    .entry(live_item.status.clone())
                    .or_default() += 1;
            }
        }

        if let Ok(doc) = Document::parse(raw_xml)
            && let Some(channel) = find_channel(&doc)
        {
            extract_persons(channel, &mut analyzer);
        }

        let first_track_title = reparsed.tracks.first().map(|track| track.title.clone());
        let remote_feed_guids: Vec<String> = reparsed
            .remote_items
            .iter()
            .map(|remote| remote.remote_feed_guid.clone())
            .collect();
        let title_key = normalized(&reparsed.title);
        let author_key = normalized(&author_name);
        let normalized_track_titles = normalized_track_titles(&reparsed);
        let release_tracklist_key = normalized_track_titles.join(" | ");
        let release_candidate_id =
            stable_cluster_id("rel", &[&title_key, &author_key, &release_tracklist_key]);
        let sample = FeedSample {
            feed_guid: row.source_db.feed_guid,
            feed_url: row.source_db.feed_url,
            host,
            platform,
            title: reparsed.title.clone(),
            author_name: reparsed.author_name.clone(),
            owner_name: reparsed.owner_name.clone(),
            track_count,
            first_track_title,
            remote_feed_guids: remote_feed_guids.clone(),
            release_candidate_id: release_candidate_id.clone(),
        };
        analyzer
            .title_author_clusters
            .entry((title_key.clone(), author_key.clone()))
            .or_default()
            .push(sample.clone());
        analyzer
            .exact_tracklist_clusters
            .entry((
                title_key.clone(),
                author_key.clone(),
                tracklist_key(&reparsed),
            ))
            .or_default()
            .push(sample.clone());
        let release_cluster = analyzer
            .release_candidate_clusters
            .entry((title_key, author_key.clone(), release_tracklist_key.clone()))
            .or_default();
        if release_cluster.normalized_track_titles.is_empty() {
            release_cluster.normalized_track_titles = normalized_track_titles;
        }
        release_cluster.feeds.push(sample.clone());
        for remote_feed_guid in remote_feed_guids {
            release_cluster.remote_feed_guids.insert(remote_feed_guid);
        }

        for track in &reparsed.tracks {
            let effective_artist_name = track
                .author_name
                .clone()
                .or_else(|| reparsed.author_name.clone());
            let track_title_key = normalized(&track.title);
            let track_artist_key = normalized(effective_artist_name.as_deref().unwrap_or("(none)"));
            analyzer
                .recording_candidate_clusters
                .entry((track_title_key, track_artist_key, track.duration_secs))
                .or_default()
                .samples
                .push(TrackSample {
                    track_guid: track.track_guid.clone(),
                    feed_guid: sample.feed_guid.clone(),
                    feed_url: sample.feed_url.clone(),
                    host: sample.host.clone(),
                    platform: sample.platform.clone(),
                    feed_title: reparsed.title.clone(),
                    release_candidate_id: release_candidate_id.clone(),
                    track_title: track.title.clone(),
                    effective_artist_name,
                    duration_secs: track.duration_secs,
                    track_number: track.track_number,
                    enclosure_url: track.enclosure_url.clone(),
                });
        }
    }

    let release_candidates_artifact = all_release_candidates(&analyzer.release_candidate_clusters);
    let recording_candidates_artifact =
        all_recording_candidates(&analyzer.recording_candidate_clusters);
    let report = build_report(analyzer);

    {
        let writer = BufWriter::new(File::create(&cli.output_json)?);
        serde_json::to_writer_pretty(writer, &report)?;
    }
    {
        let writer = BufWriter::new(File::create(&cli.output_release_json)?);
        serde_json::to_writer_pretty(writer, &release_candidates_artifact)?;
    }
    {
        let writer = BufWriter::new(File::create(&cli.output_recording_json)?);
        serde_json::to_writer_pretty(writer, &recording_candidates_artifact)?;
    }
    write_markdown(&report, &cli.output_md)?;

    eprintln!(
        "audit_analyzer: wrote {}, {}, {}, and {}",
        cli.output_md, cli.output_json, cli.output_release_json, cli.output_recording_json
    );
    Ok(())
}
